def display_bands_hyperspectral(raster, figsize, data_format='channels_last'):
    total_layers = raster.shape[2]

    if data_format != 'channels_last':
        total_layers = raster.shape[0]

    if total_layers > 1:
        fig, axs = plt.subplots(3, 3, figsize=figsize)
    else:
        fig, axs = plt.subplots(1, 2, figsize=figsize)
        
    axs_ravel = axs.ravel()
    
    for ax, i in zip(axs_ravel, range(total_layers)):
        band = i + 1
        the_title = "Band {}".format(band)
        
        if data_format != 'channels_last':
            ax.imshow(raster[i,:,:]) 
        else:
            ax.imshow(raster[:,:,i])
        
        ax.set(title=the_title)
    
    for ax in axs_ravel[total_layers:]:
            ax.set_axis_off()
            ax.set(xticks=[], yticks=[])
    
    plt.tight_layout()
    plt.show()

